package Replit;

public class NestedLoop67 {
    public static void main(String[] args) {
        int i=0;
        int j=0;
        for (i = 1; i < 10; ++i) {
            for (j = 1; j < 20; ++j) {
                System.out.print(0 + i*j);
                System.out.print(" ");
            }
        }
        System.out.println();
    }
}
